const AMU_MEV = 931.49410372

"""
    relative_isotopic_mass(Z::Int64, A::Int64)

Extract the relative atomic mass for isotope (Z, A) from NIST.

# Input Argument(s)
- `Z::Int64`: atomic number of the element.
- `A::Int64`: mass number of the isotope.

# Output Argument(s)
- `m::Float64`: relative atomic mass [u].

# Reference(s)
- NIST Atomic Weights and Isotopic Compositions.

"""
function relative_isotopic_mass(Z::Int64, A::Int64)
    key = (Z, A)
    if haskey(isotopic_mass_dict, key)
        return isotopic_mass_dict[key]
    else
        error("No relative atomic mass data for Z=$(Z), A=$(A). Please define it.")
    end
end

"""
    get_mass(Z::Int64, A::Int64)

Return the isotope mass in MeV equivalent.

# Input Argument(s)
- `Z::Int64`: atomic number of the element.
- `A::Int64`: mass number of the isotope.

# Output Argument(s)
- `m_mev::Float64`: mass [MeV].

"""
function get_mass(Z::Int64, A::Int64)
    return relative_isotopic_mass(Z, A) * AMU_MEV
end

"""
    standard_atomic_weight(Z::Int64)

Extract the atomic weight for atomic number Z ∈ {1,100}.

# Input Argument(s)
- `Z::Int64`: atomic number of the element.

# Output Argument(s)
- `A::Float64`: atomic weight [u].

# Reference(s)
- Kawrakow et al. (2021), The EGSnrc Code System: Monte Carlo Simulation of Electron and
  Photon Transport.

"""
function standard_atomic_weight(Z::Int64)
    if haskey(atomic_weight_dict, Z)
        return atomic_weight_dict[Z]
    else
        error("The input atomic number Z is invalid (Z ∉ {1,100}).")
    end
end

"""
    get_mass(Z::Int64)

Return the element mass in MeV equivalent.

# Input Argument(s)
- `Z::Int64`: atomic number of the element.

# Output Argument(s)
- `m_mev::Float64`: mass [MeV].

"""
function get_mass(Z::Int64)
    return standard_atomic_weight(Z) * AMU_MEV
end

"""
    atomic_weight(Z::Int64, Ai::Union{Nothing,Vector{Int64}}=nothing,
    atpercentAi::Union{Nothing,Vector{Float64}}=nothing)

Compute element atomic weight from isotopic composition. If isotope data are not provided,
return the standard atomic weight.

# Input Argument(s)
- `Z::Int64`: atomic number of the element.
- `Ai::Union{Nothing,Vector{Int64}}=nothing`: isotope mass numbers, or `nothing`.
- `atpercentAi::Union{Nothing,Vector{Float64}}=nothing`: isotope atomic fractions, or `nothing`.

# Output Argument(s)
- `A::Float64`: composition-weighted or standard atomic weight [u].
"""
function atomic_weight(Z::Int64, Ai::Union{Nothing,Vector{Int64}}=nothing,
                       atpercentAi::Union{Nothing,Vector{Float64}}=nothing)
    if isnothing(Ai) || isnothing(atpercentAi) || isempty(Ai) || isempty(atpercentAi)
        return standard_atomic_weight(Z)
    end

    aw = 0.0
    for (A, xA) in zip(Ai, atpercentAi)
        aw += xA * relative_isotopic_mass(Z, A)
    end

    return aw
end

"""
    molar_mass(Z::Vector{Int64}, ωz::Vector{Float64},
               A::Union{Nothing,Vector{Vector{Int64}}}=nothing,
               atpercentA::Union{Nothing,Vector{Vector{Float64}}}=nothing)

Compute the molar mass of a composition from element weight fractions.

# Input Argument(s)
- `Z::Vector{Int64}`: atomic numbers of the elements.
- `ωz::Vector{Float64}`: element weight fractions.
- `A::Union{Nothing,Vector{Vector{Int64}}}`: isotope mass numbers per element, or `nothing`.
- `atpercentA::Union{Nothing,Vector{Vector{Float64}}}`: isotope atomic fractions per element, or `nothing`.

# Output Argument(s)
- `M::Float64`: composition molar mass [u].
"""
function molar_mass(Z::Vector{Int64}, ωz::Vector{Float64},
                    A::Union{Nothing,Vector{Vector{Int64}}}=nothing,
                    atpercentA::Union{Nothing,Vector{Vector{Float64}}}=nothing)

    inv_molar_mass = 0.0
    for i in eachindex(Z)
        wi = ωz[i]

        Ai = isnothing(A) ? nothing : A[i]
        atpercentAi = isnothing(atpercentA) ? nothing : atpercentA[i]
        Ar = atomic_weight(Z[i], Ai, atpercentAi)

        inv_molar_mass += wi / Ar
    end

    inv_molar_mass > 0.0 || error("Invalid composition: sum(ωz / Mbar) must be positive.")
    return 1.0 / inv_molar_mass
end

"""
    atomic_percent_elements(Z::Vector{Int64}, ωz::Vector{Float64},
    A::Union{Nothing,Vector{Vector{Int64}}}=nothing,
    atpercentA::Union{Nothing,Vector{Vector{Float64}}}=nothing)

Convert element mass fractions into element atomic percents.

# Input Argument(s)
- `Z::Vector{Int64}`: atomic numbers of the elements.
- `ωz::Vector{Float64}`: element mass fractions.
- `A::Union{Nothing,Vector{Vector{Int64}}}`: isotope mass numbers per element, or `nothing`.
- `atpercentA::Union{Nothing,Vector{Vector{Float64}}}`: isotope atomic fractions per element, or `nothing`.

# Output Argument(s)
- `atz::Vector{Float64}`: element atomic percents (sum to 1).

# Notes
- If `A`/`atpercentA` are `nothing`, or if per-element isotope vectors are empty,
  the tabulated elemental atomic weight is used.
"""
function atomic_percent_elements(Z::Vector{Int64}, ωz::Vector{Float64},
                                 A::Union{Nothing,Vector{Vector{Int64}}}=nothing,
                                 atpercentA::Union{Nothing,Vector{Vector{Float64}}}=nothing)
    N = length(Z)

    numerators = zeros(Float64, N)
    for i in eachindex(Z)
        Zi = Z[i]
        wi = ωz[i]

        Ai = isnothing(A) ? nothing : A[i]
        atpercentAi = isnothing(atpercentA) ? nothing : atpercentA[i]
        Ar = atomic_weight(Zi, Ai, atpercentAi)

        numerators[i] = wi / Ar
    end

    denom = sum(numerators)
    denom <= 0.0 && error("Invalid composition: sum(ωz / Mbar) must be positive.")
    return numerators ./ denom
end

